package variable;

public class Var5 {

    public static void main(String[] args) {
        // 변수선언
        int a;
        a = 1;  // 변수 초기화
        System.out.println(a);

        int b = 2;  // 변수 초기화, 선언 한번에
        System.out.println(b);

        int c = 3, d = 4;
        System.out.println(c);
        System.out.println(d);
    }
}
