package variable;

public class Var7 {
    // public static void main
    public static void main(String[] args) {
        int a = 100;
        double b = 10.5;
        boolean c = true; // or false
        char d = 'A';   // 문자 하나
        String e = "Hello Java";    // 문자열을 다루기 위한 특별한 타입, string에 s 꼭 대문자로

        // cmd + d 는 한줄이 바로 복사된다
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}

/*
리터럴

코드에서 개발자가 직접 적은 변수들
 int a = 100;
        double b = 10.5;
        boolean c = true; // or false
        char d = 'A';   // 문자 하나
        String e = "Hello Java";
와 같은 고정된 값들을 리터럴이라 한다.
정수 리터럴, 실수 리터럴, 불리언 리터럴 등등...
변수의 값은 변할 수 있지만 리터럴은 개발자가 직접 입력한 고정된 값이다. 따라서 리터럴 자체는 변하지 않는다.
 */
