package method;

public class Method1 {

    public static void main(String[] args) {
        // 계산 1
        int a = 1;
        int b = 2;
        int sum1 = a + b;
        System.out.println(a + " + " + b + " 연산 수행 ");
        System.out.println(a + " + " + b + " = " + sum1);

        // 계산 2
        int x = 10;
        int y = 20;
        int sum2 = x + y;
        System.out.println(x + " + " + y + " 연산 수행 ");
        System.out.println(x + " + " + y + " = " + sum2);
    }
}
